// Вывести все числа, которые заканчиваются на 3, в интервале от a до b. (значения a и b вводятся пользователем). Вычислить их сумму и количество.


class Program
{
    static void Main()
    {
        // Запрашиваем у пользователя ввести начало (a) и конец (b) интервала
        Console.Write("Введите начало интервала (a): ");
        int a = int.Parse(Console.ReadLine());

        Console.Write("Введите конец интервала (b): ");
        int b = int.Parse(Console.ReadLine());

        int sum = 0; // Переменная для хранения суммы чисел, заканчивающихся на 3
        int count = 0; // Переменная для хранения количества чисел, заканчивающихся на 3

        Console.WriteLine($"Числа, заканчивающиеся на 3, в интервале от {a} до {b}:");

        for (int number = a; number <= b; number++)
        {
            if (number % 10 == 3) // Проверяем, заканчивается ли число на 3
            {
                Console.Write(number + " "); // Выводим число
                sum += number; // Добавляем число к сумме
                count++; // Увеличиваем счетчик
            }
        }

        Console.WriteLine(); // Переходим на новую строку

        Console.WriteLine($"Сумма чисел: {sum}");
        Console.WriteLine($"Количество чисел: {count}");
    }
}
