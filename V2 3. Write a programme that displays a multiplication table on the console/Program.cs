// Напишите программу, которая выводит на консоль таблицу умножения

class Program
{
    static void Main()
    {
        Console.WriteLine("Таблица умножения от 1 до 10:\n");

        // Выводим заголовок для столбцов (числа от 1 до 10)
        Console.Write("   "); // Добавляем пустой столбец для выравнивания
        for (int i = 1; i <= 10; i++)
        {
            Console.Write($"{i,4}"); // Выводим числа от 1 до 10 с одинаковым отступом
        }
        Console.WriteLine("\n-------------------------------------------");

        // Вложенный цикл для умножения чисел от 1 до 10 на числа от 1 до 10
        for (int i = 1; i <= 10; i++)
        {
            Console.Write($"{i,2} |"); // Выводим заголовок для строк с одинаковым отступом
            for (int j = 1; j <= 10; j++)
            {
                // Выводим результат умножения i на j с одинаковым отступом
                Console.Write($"{i * j,4}");
            }
            Console.WriteLine(); // Переход на новую строку после умножения на все числа от 1 до 10
        }
    }
}
