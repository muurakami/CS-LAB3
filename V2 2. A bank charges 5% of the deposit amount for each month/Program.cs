// За каждый месяц банк начисляет к сумме вклада 5% от суммы. Напишите консольную программу, в которую пользователь вводит сумму вклада и количество месяцев. А банк вычисляет конечную сумму вклада с учетом начисления процентов за каждый месяц.

class Program
{
    static void Main()
    {
        // Запрашиваем у пользователя ввести сумму вклада
        Console.Write("Введите сумму вклада: ");
        double initialAmount = double.Parse(Console.ReadLine());

        // Запрашиваем у пользователя ввести количество месяцев
        Console.Write("Введите количество месяцев: ");
        int months = int.Parse(Console.ReadLine());

        double interestRate = 0.05; // Годовая процентная ставка (5%)

        // Вычисляем конечную сумму вклада с учетом начисления процентов за каждый месяц
        for (int i = 0; i < months; i++)
        {
            initialAmount += initialAmount * interestRate / 12; // Расчет процентов за месяц
        }

        Console.WriteLine($"Конечная сумма вклада после {months} месяцев: {initialAmount:C}");
    }
}
