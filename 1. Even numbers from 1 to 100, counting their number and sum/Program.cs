// 1. Напишите программу, выводящую на экран все четные числа от 1 до 100, подсчитывающую их количество и сумму. 

class Program
{
    static void Main()
    {
        // Объявляем переменные для подсчета суммы и количества четных чисел
        int sum = 0;
        int count = 0;

        // Используем цикл for для прохода по числам от 1 до 100
        for (int number = 1; number <= 100; number++)
        {
            // Проверяем, является ли текущее число четным
            if (number % 2 == 0)
            {
                // Если число четное, выводим его на экран
                Console.WriteLine(number);

                // Увеличиваем сумму на значение текущего числа
                sum += number;

                // Увеличиваем счетчик четных чисел на 1
                count++;
            }
        }

        // Выводим информацию о сумме и количестве четных чисел
        Console.WriteLine($"Сумма четных чисел: {sum}");
        Console.WriteLine($"Количество четных чисел: {count}");
    }
}
